import UIKit

public extension CGColor {
  public static var clear: CGColor { return UIColor.clear.cgColor }
  public static var red: CGColor { return UIColor.red.cgColor }
  public static var blue: CGColor { return UIColor.blue.cgColor }
  public static var green: CGColor { return UIColor.green.cgColor }
  public static var yellow: CGColor { return UIColor.yellow.cgColor }
}
